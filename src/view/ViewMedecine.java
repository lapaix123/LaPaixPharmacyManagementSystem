    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int choice= JOptionPane.showConfirmDialog(this, "Do you want to delete this user", "Chose yes/no", JOptionPane.YES_NO_OPTION);
          if(choice==0){
          try{
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Pharmacy_Managment_system","root","");
          Statement st=con.createStatement();
          String query="delete from medecine where id="+id+"";
          int counter=st.executeUpdate(query);
              if (counter>=1) {
                 JOptionPane.showMessageDialog(this,"user deleted!");
                 tbm.setRowCount(0);
                 displayMedecine();
              }
              else{
                  JOptionPane.showMessageDialog(this,"user not deleted!");
              }
          }catch(Exception e){
            e.printStackTrace();
          }  }
        
    }//GEN-LAST:event_jButton1ActionPerformed

