    private void displayMedecine() {
        try {
            MedecineDao user = new MedecineDao();
            ResultSet rst = user.displayMedecine();
            while (rst.next()) {
                tbm.addRow(new Object[]{
                    rst.getString(1),
                    rst.getString(2),
                    rst.getString(3),
                    rst.getString(4),
                    rst.getString(5),
                    rst.getString(6),
                    rst.getString(7),
                    rst.getString(8)
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

