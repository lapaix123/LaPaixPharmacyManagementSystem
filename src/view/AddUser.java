    private void userNameTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_userNameTxtKeyReleased
        // TODO add your handling code here:
        String username = userNameTxt.getText();
        if (!username.equals("")) {
            iconLabel.setVisible(true);
            iconLabel.setIcon(new ImageIcon("src\\images\\yes.png"));
            iconLabel.setText("");
            checkusername = 0;
            User user = new User();
            user.setUsername(userNameTxt.getText());
            UserDao dao = new UserDao();
            int counter = dao.userNameCheck(user);
            if (counter >= 1) {
                iconLabel.setIcon(new ImageIcon("src\\images\\no.png"));
                checkusername = 1;
            }

        }
    }//GEN-LAST:event_userNameTxtKeyReleased

