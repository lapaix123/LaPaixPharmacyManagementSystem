    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-mm-yyyy");
        Date date=dobDate.getDate();
        String dob="";
        if(date!= null){
            dob=dateFormat.format(dobDate.getDate());
        }
        
        if (nameTxt.getText().trim().isEmpty() || dob.equals(null)
                || phoneTxt.getText().trim().isEmpty() || emailTxt.getText().trim().isEmpty()
                || addressTxt.getText().trim().isEmpty() || userNameTxt.getText().trim().isEmpty()||userTxt.getSelectedItem().equals("---Select user typ--") ){
            JOptionPane.showMessageDialog(this, "Plz fill all data!!", "all data error", JOptionPane.WARNING_MESSAGE);
        } 
        else {
            if (!emailTxt.getText().matches(emailPat)) {
                JOptionPane.showMessageDialog(this, "Email is not valid", "email error", JOptionPane.WARNING_MESSAGE);
            } else {
                if (checkusername == 1) {
                    JOptionPane.showMessageDialog(this, "User name alredy exits ", "user name error", JOptionPane.WARNING_MESSAGE);
                } else {

                    if (!phoneTxt.getText().matches(phonpat)) {
                        JOptionPane.showMessageDialog(this, "your phone is not valid ", "phone error", JOptionPane.WARNING_MESSAGE);
                    }else if(phoneTxt.getText().length()!=10 ||!phoneTxt.getText().substring(0,2).equals("07")){
                        JOptionPane.showMessageDialog(this, "phone number muster start with 07 and mustbe 10");
                    }
                    else {
                        if(passwordTxt.getText().length()>=8 && passwordTxt.getText().length()<=18){

                        User user = new User();
                        user.setUserRore(userTxt.getSelectedItem().toString());
                        user.setName(nameTxt.getText());
                        user.setDob(dob);
                        user.setPhone(phoneTxt.getText());
                        user.setEmail(emailTxt.getText());
                        user.setUsername(userNameTxt.getText());
                        user.setPassword(passwordTxt.getText());
                        user.setAddress(addressTxt.getText());
                        UserDao dao = new UserDao();
                        int counter = dao.newUser(user);
                        if (counter >= 1) {
                            JOptionPane.showMessageDialog(this, "USER SAVED SUCESS");
                            nameTxt.setText(null);
                            emailTxt.setText(null);
                            phoneTxt.setText(null);
                            userNameTxt.setText(null);
                            passwordTxt.setText(null);
                            addressTxt.setText(null);
                            dobDate.setDate(null);
                            userTxt.setSelectedIndex(0);
                        } else {
                            JOptionPane.showMessageDialog(this, "USER NOT SAVED");
                        }

                    }else{
                            JOptionPane.showMessageDialog(this, "Password Must be between 8 and 16","password length error",JOptionPane.WARNING_MESSAGE);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

